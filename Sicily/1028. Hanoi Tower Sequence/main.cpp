#include "iostream"
using namespace std;

bool div(string& s) {
  if ((s[s.size() - 1] - '0') % 2 != 0) return false;
  int carry = 0, next_carry = 0;
  for (int i = 0; i < s.size() - 1; ++i) {
    next_carry = (s[i] - '0') % 2;
    s[i] = (s[i] - '0' + carry * 10) / 2 + '0';
    carry = next_carry;
  }
  s[s.size() - 1] = (s[s.size() - 1] - '0' + carry * 10) / 2 + '0';
  return true;
}


int main() {
  int n, count = 1;
  cin >> n;
  while (n--) {
    if (count > 1) cout << endl;
    int ans = 1;
    string p;
    cin >> p;
    while (div(p)) {
      ++ans;
    }
    cout << "Case " << count << ": " << ans << endl;
    ++count;
  }
}


